-- University Telegram Bot


CREATE TYPE semester_type     AS ENUM ('Fall', 'Spring', 'Summer');
CREATE TYPE session_kind      AS ENUM ('Lecture', 'Section', 'Lab');
CREATE TYPE week_day          AS ENUM ('Sunday','Monday','Tuesday','Wednesday',
                                       'Thursday','Friday','Saturday');
CREATE TYPE attendance_status AS ENUM ('Present','Absent','Late','Excused');
CREATE TYPE gender_type       AS ENUM ('Male','Female','Other');
CREATE TYPE grade_type        AS ENUM ('A+','A','A-','B+','B','B-',
                                       'C+','C','C-','D+','D','F','W','I','P');




CREATE OR REPLACE FUNCTION fn_touch_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    NEW.Updated_At = NOW();
    RETURN NEW;
END;
$$;


-- SECTION 2 — UNIVERSITY HIERARCHY

CREATE TABLE Universities (
    University_Id       INT         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    University_Name     VARCHAR(100) NOT NULL,
    University_Location VARCHAR(200),
    Created_At          TIMESTAMPTZ  DEFAULT NOW(),
    Updated_At          TIMESTAMPTZ  DEFAULT NOW()
);
CREATE TRIGGER trg_universities_upd
    BEFORE UPDATE ON Universities
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


CREATE TABLE Faculties (
    Faculty_Id       INT         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    University_Id    INT         NOT NULL REFERENCES Universities(University_Id) ON DELETE RESTRICT,
    Faculty_Name     VARCHAR(100) NOT NULL,
    Faculty_Location VARCHAR(200),
    Created_At       TIMESTAMPTZ DEFAULT NOW(),
    Updated_At       TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX idx_faculties_university ON Faculties(University_Id);
CREATE TRIGGER trg_faculties_upd
    BEFORE UPDATE ON Faculties
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


CREATE TABLE Departments (
    Department_Id   INT         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Faculty_Id      INT         NOT NULL REFERENCES Faculties(Faculty_Id) ON DELETE RESTRICT,
    Department_Name VARCHAR(100) NOT NULL,
    Created_At      TIMESTAMPTZ DEFAULT NOW(),
    Updated_At      TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX idx_departments_faculty ON Departments(Faculty_Id);
CREATE TRIGGER trg_departments_upd
    BEFORE UPDATE ON Departments
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


-- SECTION 3 — PROFESSORS

CREATE TABLE Professors (
    Professor_Id    INT         GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Department_Id   INT         NOT NULL REFERENCES Departments(Department_Id) ON DELETE RESTRICT,
    Professor_Name  VARCHAR(100) NOT NULL,
    Professor_Email VARCHAR(150) NOT NULL UNIQUE,
    Academic_Title  VARCHAR(100),
    Created_At      TIMESTAMPTZ DEFAULT NOW(),
    Updated_At      TIMESTAMPTZ DEFAULT NOW()
);
CREATE INDEX idx_professors_department ON Professors(Department_Id);
CREATE TRIGGER trg_professors_upd
    BEFORE UPDATE ON Professors
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


-- SECTION 4 — STUDENTS

CREATE TABLE Students (
    Student_Id        INT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    National_Id       VARCHAR(20)  UNIQUE,            -- VARCHAR: national IDs can have leading zeros
    Student_Name      VARCHAR(100) NOT NULL,
    Phone             VARCHAR(20),                    -- VARCHAR: country codes, +20 prefix, etc.
    Address           VARCHAR(200),
    Birthdate         DATE,                           -- DATE: was incorrectly INT
    Gender            gender_type,
    Study_Year        SMALLINT CHECK (Study_Year BETWEEN 1 AND 7), -- student's year of study (1st–7th)
    Department_Id     INT          NOT NULL REFERENCES Departments(Department_Id) ON DELETE RESTRICT,
    Enrollment_Year   SMALLINT,                       -- calendar year of first enrollment
    Telegram_Id       BIGINT       UNIQUE,            -- BIGINT: Telegram IDs exceed INT range (~7–10 digits)
    Telegram_Username VARCHAR(100) UNIQUE,            -- Telegram max is 32 chars; 100 is a safe buffer
    Is_Active         BOOLEAN      NOT NULL DEFAULT TRUE, -- soft-delete flag
    Created_At        TIMESTAMPTZ  DEFAULT NOW(),
    Updated_At        TIMESTAMPTZ  DEFAULT NOW()
);

CREATE INDEX idx_students_telegram_id       ON Students(Telegram_Id);
CREATE INDEX idx_students_telegram_username ON Students(Telegram_Username);
CREATE INDEX idx_students_department        ON Students(Department_Id);
CREATE INDEX idx_students_national_id       ON Students(National_Id);
CREATE INDEX idx_students_active_tg         ON Students(Telegram_Id) WHERE Is_Active = TRUE;

CREATE TRIGGER trg_students_upd
    BEFORE UPDATE ON Students
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


-- SECTION 5 — COURSES

CREATE TABLE Courses (
    Course_Id     INT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Department_Id INT          NOT NULL REFERENCES Departments(Department_Id) ON DELETE RESTRICT,
    Course_Code   VARCHAR(20)  UNIQUE,                -- e.g. CS301, MATH201 — added for display & lookup
    Course_Name   VARCHAR(100) NOT NULL,
    Credit_Hours  SMALLINT     CHECK (Credit_Hours BETWEEN 1 AND 6),
    Study_Year    SMALLINT,                           -- which year of study this course targets
    Semester      semester_type,
    Created_At    TIMESTAMPTZ  DEFAULT NOW(),
    Updated_At    TIMESTAMPTZ  DEFAULT NOW()
);
CREATE INDEX idx_courses_department ON Courses(Department_Id);
CREATE INDEX idx_courses_semester   ON Courses(Study_Year, Semester);
CREATE TRIGGER trg_courses_upd
    BEFORE UPDATE ON Courses
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


-- SECTION 6 — SESSIONS (specific recurring class meetings)

CREATE TABLE Sessions (
    Session_Id    INT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Course_Id     INT          NOT NULL REFERENCES Courses(Course_Id)    ON DELETE RESTRICT,
    Professor_Id  INT          NOT NULL REFERENCES Professors(Professor_Id) ON DELETE RESTRICT,
    Session_Type  session_kind NOT NULL,
    Day_Of_Week   week_day,
    Start_Time    TIME,
    End_Time      TIME,                               -- was missing in original
    Location      VARCHAR(100),
    Semester      semester_type,
    Academic_Year SMALLINT,                           -- calendar year (e.g. 2024 or 2025)
    Created_At    TIMESTAMPTZ  DEFAULT NOW(),
    Updated_At    TIMESTAMPTZ  DEFAULT NOW(),

    CONSTRAINT uq_room_slot UNIQUE NULLS NOT DISTINCT
        (Location, Day_Of_Week, Start_Time, Semester, Academic_Year)
);
CREATE INDEX idx_sessions_course    ON Sessions(Course_Id);
CREATE INDEX idx_sessions_professor ON Sessions(Professor_Id);
CREATE INDEX idx_sessions_schedule  ON Sessions(Day_Of_Week, Start_Time);
CREATE INDEX idx_sessions_semester  ON Sessions(Semester, Academic_Year);
CREATE TRIGGER trg_sessions_upd
    BEFORE UPDATE ON Sessions
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();


-- SECTION 7 — STUDENT REGISTRATIONS (semester-level confirmation)

CREATE TABLE Student_Registrations (
    Registration_Id   INT           GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Student_Id        INT           NOT NULL REFERENCES Students(Student_Id) ON DELETE RESTRICT,
    Semester          semester_type NOT NULL,
    Academic_Year     VARCHAR(9)    NOT NULL,          -- e.g. '2024/2025'
    Registration_Date TIMESTAMPTZ   DEFAULT NOW(),
    Status            VARCHAR(20)   NOT NULL DEFAULT 'Active'
                          CHECK (Status IN ('Active','Withdrawn','Suspended')),
    Created_At        TIMESTAMPTZ   DEFAULT NOW(),
    UNIQUE (Student_Id, Semester, Academic_Year)
);
CREATE INDEX idx_student_reg_student  ON Student_Registrations(Student_Id);
CREATE INDEX idx_student_reg_semester ON Student_Registrations(Student_Id, Semester, Academic_Year);


-- SECTION 8 — STUDENT COURSES (course-level enrollment)

CREATE TABLE Student_Courses (
    Student_Id        INT           NOT NULL REFERENCES Students(Student_Id) ON DELETE RESTRICT,
    Course_Id         INT           NOT NULL REFERENCES Courses(Course_Id)   ON DELETE RESTRICT,
    Semester          semester_type NOT NULL,
    Academic_Year     VARCHAR(9)    NOT NULL,           -- e.g. '2024/2025'
    Registration_Date TIMESTAMPTZ   DEFAULT NOW(),
    -- Composite PK was missing in the original schema
    PRIMARY KEY (Student_Id, Course_Id, Semester, Academic_Year)
);
CREATE INDEX idx_student_courses_course   ON Student_Courses(Course_Id);
CREATE INDEX idx_student_courses_semester ON Student_Courses(Student_Id, Semester, Academic_Year);


-- SECTION 9 — ENROLLMENTS (session-level: student ↔ specific session)

CREATE TABLE Enrollments (
    Enrollment_Id INT           GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Student_Id    INT           NOT NULL REFERENCES Students(Student_Id) ON DELETE RESTRICT,
    Session_Id    INT           NOT NULL REFERENCES Sessions(Session_Id) ON DELETE RESTRICT,
    Semester      semester_type,
    Grade         grade_type,
    Gpa           NUMERIC(3,2)  CHECK (Gpa BETWEEN 0.00 AND 4.00), -- NUMERIC avoids float rounding errors
    Created_At    TIMESTAMPTZ   DEFAULT NOW(),
    Updated_At    TIMESTAMPTZ   DEFAULT NOW(),
    UNIQUE (Student_Id, Session_Id)                    -- no duplicate enrollments
);
CREATE INDEX idx_enrollments_student  ON Enrollments(Student_Id);
CREATE INDEX idx_enrollments_session  ON Enrollments(Session_Id);
CREATE INDEX idx_enrollments_semester ON Enrollments(Student_Id, Semester);
CREATE TRIGGER trg_enrollments_upd
    BEFORE UPDATE ON Enrollments
    FOR EACH ROW EXECUTE FUNCTION fn_touch_updated_at();



-- SECTION 13 — BOT STATES  [NEW TABLE]


CREATE TABLE Bot_States (
    Telegram_Chat_Id BIGINT       NOT NULL PRIMARY KEY,  -- one active state per chat
    Student_Id       INT          REFERENCES Students(Student_Id) ON DELETE CASCADE,
    Current_State    VARCHAR(100) NOT NULL DEFAULT 'IDLE',

    State_Data       JSONB,                             -- flexible payload for multi-step flows
    Last_Updated     TIMESTAMPTZ  DEFAULT NOW(),
    Expires_At       TIMESTAMPTZ  DEFAULT NOW() + INTERVAL '2 hours' -- auto-expire stale sessions
);
CREATE INDEX idx_bot_states_student ON Bot_States(Student_Id);
CREATE INDEX idx_bot_states_expires ON Bot_States(Expires_At);   -- for cleanup cron job
CREATE INDEX idx_bot_states_data    ON Bot_States USING GIN(State_Data); -- for JSONB queries


-- SECTION 14 — UTILITY FUNCTIONS

CREATE OR REPLACE FUNCTION fn_upsert_bot_state(
    p_chat_id    BIGINT,
    p_student_id INT,
    p_state      VARCHAR,
    p_data       JSONB DEFAULT NULL
)
RETURNS VOID LANGUAGE plpgsql AS $$
BEGIN
    INSERT INTO Bot_States(Telegram_Chat_Id, Student_Id, Current_State, State_Data)
    VALUES (p_chat_id, p_student_id, p_state, p_data)
    ON CONFLICT (Telegram_Chat_Id) DO UPDATE SET
        Student_Id    = EXCLUDED.Student_Id,
        Current_State = EXCLUDED.Current_State,
        State_Data    = EXCLUDED.State_Data,
        Last_Updated  = NOW(),
        Expires_At    = NOW() + INTERVAL '2 hours';
END;
$$;


CREATE OR REPLACE FUNCTION fn_purge_expired_bot_states()
RETURNS INTEGER LANGUAGE plpgsql AS $$
DECLARE deleted_count INTEGER;
BEGIN
    DELETE FROM Bot_States WHERE Expires_At < NOW();
    GET DIAGNOSTICS deleted_count = ROW_COUNT;
    RETURN deleted_count;
END;
$$;

CREATE OR REPLACE FUNCTION fn_get_student_by_telegram(p_telegram_id BIGINT)
RETURNS TABLE (
    Student_Id   INT,
    Student_Name VARCHAR,
    Study_Year   SMALLINT,
    Is_Active    BOOLEAN
) LANGUAGE sql STABLE AS $$
    SELECT Student_Id, Student_Name, Study_Year, Is_Active
    FROM   Students
    WHERE  Telegram_Id = p_telegram_id
    LIMIT  1;
$$;


-- SECTION 15 — MATERIALIZED VIEW: Student Weekly Schedule


CREATE MATERIALIZED VIEW mv_student_schedule AS
SELECT
    s.Student_Id,
    s.Telegram_Id,
    s.Student_Name,
    c.Course_Code,
    c.Course_Name,
    ses.Session_Id,
    ses.Session_Type,
    ses.Day_Of_Week,
    ses.Start_Time,
    ses.End_Time,
    ses.Location,
    p.Professor_Name,
    e.Semester,
    ses.Academic_Year
FROM   Students      s
JOIN   Enrollments   e   ON s.Student_Id  = e.Student_Id
JOIN   Sessions      ses ON e.Session_Id  = ses.Session_Id
JOIN   Courses       c   ON ses.Course_Id = c.Course_Id
JOIN   Professors    p   ON ses.Professor_Id = p.Professor_Id
WHERE  s.Is_Active = TRUE;

CREATE UNIQUE INDEX idx_mv_schedule_pk
    ON mv_student_schedule(Student_Id, Session_Id);
CREATE INDEX idx_mv_schedule_telegram
    ON mv_student_schedule(Telegram_Id);
CREATE INDEX idx_mv_schedule_day
    ON mv_student_schedule(Telegram_Id, Day_Of_Week, Start_Time);

